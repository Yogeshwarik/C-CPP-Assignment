#include <stdio.h>

int main()
{
    int i, n;
    printf("Enter a number: ");
    scanf("%d", &n);
    for(i = 0; i <= n; a++)
    {
        if (i == a * a)
        {
            printf("perfect square");
            break;
        }
    }
    printf("not perfect square");
    return 0;
}
